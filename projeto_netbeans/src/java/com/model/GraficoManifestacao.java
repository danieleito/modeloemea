package com.model;

/**
 *
 * @author Daniele Harumi Ito
 */
public class GraficoManifestacao {

    private int qtde;
    private String nome;

    public GraficoManifestacao(int qtde, String nome) {
        this.qtde = qtde;
        this.nome = nome;
    }

    public int getQtde() {
        return qtde;
    }

    public void setQtde(int qtde) {
        this.qtde = qtde;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
    
    
}
